//Доколку збирот на првите два броја е парен број и збирот на
//последните два броја е непарен број, да се испечати 1, во спротивно
// да се испечати 0.

#include <iostream>
using namespace std;
int main()
{
    int a,b,c,d,z1=0,z2=0;
    cin>>a>>b>>c>>d;
    z1=a+b;
    z2=c+d;
    if (z1%2==0 && z2%2!=0)
    {
        cout<<1;
    }
    else
    {
        cout<<0;
    }
    return 0;
}